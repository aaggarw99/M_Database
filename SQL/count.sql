(SELECT COUNT(*) FROM INSTRUMENT) AS AmountOfInstruments, 
(SELECT COUNT(*) FROM DAW) AS AmountOfDaws, 
(SELECT COUNT(*) FROM FAVORITE_ARTIST) AS AmountOfFavoriteArtists,
(SELECT COUNT(*) FROM ARTIST) AS AmountOfArtists,
(SELECT COUNT(*) FROM GENRE) AS AmountOfGenres,
(SELECT COUNT(*) FROM PLAYS) AS AmountOfPlays,
(SELECT COUNT(*) FROM BELONGS) AS AmountOfBelongs,
(SELECT COUNT(*) FROM LOVES) AS AmountOfLoves,
(SELECT COUNT(*) FROM LIKES) AS AmountOfLikes,
(SELECT COUNT(*) FROM USES) AS AmountOfUses,
(SELECT COUNT(*) FROM LISTENS) AS AmountOfListens
